<p class="accueil">
		Smart Garden est un site web qui vous permet de gérer un jardin, et plus particulièrement les plantes qui y poussent ! <br/> 
		Vous avez à disposition plusieurs fonctionnalités disponibles dans le menu pour plus de facilité. <br/> <br/>
		Vous pouvez : <br/> 
		<ul> 
			<li>afficher des variétés, des plantes et des types de plantes</li>
			<li>ajouter des variétés</li>
			<li>générer et afficher une parcelle d'un jardin</li>
			<li>ajouter une photo d'une plante</li>
			<li>afficher l'album photos d'une plante</li>
			<li>supprimer une photo d'une plante</li>
		</ul>
		<br/>
		De nouvelles fonctionnalités sont en cours de développement et devraient bientôt être disponibles ! 
